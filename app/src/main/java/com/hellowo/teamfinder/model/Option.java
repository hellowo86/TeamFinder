package com.hellowo.teamfinder.model;

public class Option {
    int id;
    String name;
    int iconId;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getIconId() {
        return iconId;
    }

    public void setIconId(int iconId) {
        this.iconId = iconId;
    }

    public String makeTag(){
        return "#" + name + " ";
    }
}
